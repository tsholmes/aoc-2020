func part2() {
	lines := aoc.Lines(input)
	res := 0
	const (
		tNum = iota
		tMul
		tAdd
		tOparen
		tCparen
	)
	type element struct {
		typ int
		val int
	}
	solve := func(s string) int {
		var els []element
		for _, c := range []byte(s) {
			if c == ' ' {
				continue
			}
			switch c {
			case '(':
				els = append(els, element{typ: tOparen})
			case ')':
				els = append(els, element{typ: tCparen})
			case '+':
				els = append(els, element{typ: tAdd})
			case '*':
				els = append(els, element{typ: tMul})
			default:
				els = append(els, element{typ: tNum, val: int(c - '0')})
			}
		}
		var eval func([]element) int
		eval = func(es []element) int {
			var nes []element
			for len(es) > 0 {
				e := es[0]
				if e.typ == tOparen {
					depth := 1
					var i = 0
					for depth > 0 {
						i++
						if es[i].typ == tOparen {
							depth++
						} else if es[i].typ == tCparen {
							depth--
						}
					}
					nes = append(nes, element{typ: tNum, val: eval(es[1:i])})
					es = es[i+1:]
				} else {
					nes = append(nes, e)
					es = es[1:]
				}
			}
			es = nes

			for {
				nes = nil
				hasAdd := false
				skip := false
				for i, e := range es {
					if skip {
						skip = false
						continue
					}
					if e.typ == tAdd {
						nes[len(nes)-1] = element{typ: tNum, val: nes[len(nes)-1].val + es[i+1].val}
						skip = true
						hasAdd = true
					} else {
						nes = append(nes, e)
					}
				}
				es = nes
				if !hasAdd {
					break
				}
			}

			res := 1
			for i := 0; i < len(es); i += 2 {
				res = res * es[i].val
			}

			return res
		}

		return eval(els)
	}
	for _, line := range lines {
		r := solve(line)
		res += r
	}
	fmt.Println(res)
}

